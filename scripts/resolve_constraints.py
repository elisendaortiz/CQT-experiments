#!/usr/bin/env python3
"""
resolve_constraints.py

Reads compatibility_matrix.toml, picks the latest known-good combo, and
determines which packages need to be pinned based on what's being installed
as editable forks vs what pip should resolve from PyPI.

Usage:
    python3 scripts/resolve_constraints.py <repo1> [<repo2> ...]

    Repo names are folder names under ~/, e.g.:
        qibocal-eo-fork  CQT-experiments-eo-fork

Output lines (consumed by setup_dev_env.sh):
    CONSTRAINTS_FILE /tmp/pip_constraints_xxx.txt
    PIN qibo==0.2.23
    PIN qibolab==0.2.9
    ...

Packages in the combo that match one of the editable repos being installed
are NOT pinned (they will be installed from local source at whatever version
is in that repo). Everything else in the combo is pinned exactly.

Exit codes:
    0 — success (constraints file always written, may be empty)
    1 — compatibility_matrix.toml not found
"""

import re
import sys
import tempfile
from pathlib import Path

try:
    import tomllib

    def _load_toml(path: Path) -> dict:
        with open(path, "rb") as f:
            return tomllib.load(f)

except ImportError:
    # Python < 3.11: minimal parser sufficient for our specific TOML structure.
    # Handles [[combos]] section headers and key = "string value" pairs only.
    def _load_toml(path: Path) -> dict:
        combos = []
        current = None
        with open(path) as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith("#"):
                    continue
                if line == "[[combos]]":
                    current = {}
                    combos.append(current)
                elif current is not None:
                    m = re.match(r'^(\w+)\s*=\s*"([^"]*)"$', line)
                    if m:
                        current[m.group(1)] = m.group(2)
        return {"combos": combos}


MATRIX_FILE = Path(__file__).parent.parent / "compatibility_matrix.toml"

# Maps PyPI package names to the repo folder name patterns that provide them.
# If a repo in the install list matches any of these patterns, the package
# is treated as "provided by editable install" and not pinned from PyPI.
REPO_TO_PACKAGE = {
    "qibocal": "qibocal",
    "qibolab": "qibolab",
    "qibo":    "qibo",
}


def package_from_repo(repo_name: str):
    """Return the PyPI package name for a given repo folder name, if known."""
    for pkg, fragment in REPO_TO_PACKAGE.items():
        if fragment in repo_name.lower():
            return pkg
    return None


def main():
    repos = list(sys.argv[1:])

    if not repos:
        print("ERROR: no repos provided", file=sys.stderr)
        sys.exit(1)

    if not MATRIX_FILE.exists():
        print(f"ERROR: compatibility matrix not found at {MATRIX_FILE}", file=sys.stderr)
        sys.exit(1)

    matrix = _load_toml(MATRIX_FILE)

    combos = matrix.get("combos", [])
    if not combos:
        print("ERROR: no combos defined in compatibility_matrix.toml", file=sys.stderr)
        sys.exit(1)

    # Pick the latest combo by date
    active = sorted(combos, key=lambda c: c["date"])[-1]
    print(
        f"INFO: using combo dated {active['date']} — {active.get('description', '')}",
        file=sys.stderr,
    )

    # Packages provided by editable installs — skip pinning these
    editable_packages = {package_from_repo(r) for r in repos} - {None}

    # Everything else in the combo gets pinned
    pins = {}
    for pkg, version in active.items():
        if pkg in ("date", "description"):
            continue
        if pkg in editable_packages:
            print(f"INFO: skipping pin for '{pkg}' — provided by editable install", file=sys.stderr)
        else:
            pins[pkg] = version

    # Write constraints file (pip -c format: one specifier per line)
    tmp = tempfile.NamedTemporaryFile(
        mode="w", prefix="pip_constraints_", suffix=".txt", delete=False
    )
    tmp.write(f"# Auto-generated by resolve_constraints.py\n")
    tmp.write(f"# Combo: {active['date']} — {active.get('description', '')}\n")
    for pkg, version in sorted(pins.items()):
        tmp.write(f"{pkg}=={version}\n")
    tmp.close()

    print(f"CONSTRAINTS_FILE {tmp.name}")
    for pkg, version in sorted(pins.items()):
        print(f"PIN {pkg}=={version}")


if __name__ == "__main__":
    main()
